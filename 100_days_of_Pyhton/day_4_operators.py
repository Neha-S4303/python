
num1 = 10
num2 = 3

# Aritmetic Operators
print("\n --- Arithmetic Operators---")
print("Addition", num1 + num2)
print("Subtraction", num1 - num2)
print("Multiplication", num1 * num2)
print("Division", num1 / num2)
print("Floor Division", num1 // num2)
print("Modulus", num1 % num2)
print("Exponentiation", num1 ** num2)

# Relational Operators
print("\n --- Relational Operators---")
print("Is 10 equal to 3?", num1 == num2)
print("Is 10 not equal to 3?", num1 != num2)
print("Is 10 greater than 3?", num1 > num2)
print("Is 10 less than 3?", num1 < num2)
print("Is 10 greater than equal to  3?", num1 >= num2)
print("Is 10 less than equal to 3?", num1 <= num2)

# Logical Operators
true_value = True
false_value = False

print("\n --- Logical Operators---")
print("True and False", true_value and false_value)
print("True and True", true_value and true_value)
print("False and False", false_value and false_value)
print("True or False", true_value or false_value)
print("True or True", true_value or true_value)
print("False and False", false_value and false_value)
print("Not true", not true_value)
print("Not False", not false_value)
